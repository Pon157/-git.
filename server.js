// –§—É–Ω–∫—Ü–∏–∏ —Ä–∞–±–æ—Ç—ã —Å –¥–∞–Ω–Ω—ã–º–∏
function getUsers() {
    try {
        return JSON.parse(fs.readFileSync(USERS_FILE, 'utf8'));
    } catch {
        return [];
    }
}

function getChats() {
    try {
        return JSON.parse(fs.readFileSync(CHATS_FILE, 'utf8'));
    } catch {
        return [];
    }
}

function saveUsers(users) {
    fs.writeFileSync(USERS_FILE, JSON.stringify(users, null, 2));
}

function saveChats(chats) {
    fs.writeFileSync(CHATS_FILE, JSON.stringify(chats, null, 2));
}

function generateId() {
    return Date.now().toString() + Math.random().toString(36).substr(2, 9);
}

function getUserById(userId) {
    return getUsers().find(u => u.id === userId);
}

function updateUser(userId, updates) {
    const users = getUsers();
    const userIndex = users.findIndex(u => u.id === userId);
    if (userIndex !== -1) {
        users[userIndex] = { ...users[userIndex], ...updates };
        saveUsers(users);
        return users[userIndex];
    }
    return null;
}

// Socket.IO
io.on('connection', (socket) => {
    console.log('üîó –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ:', socket.id);

    // –û—Ç–ø—Ä–∞–≤–∫–∞ –Ω–∞—á–∞–ª—å–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
    socket.emit('users_list', { users: getUsers().map(u => {
        const { password, socketId, ...user } = u;
        return user;
    })});
    socket.emit('chats_list', { chats: getChats() });

    // –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–µ—Å—Å–∏–∏
    socket.on('restore_session', (data) => {
        const user = getUserById(data.userId);
        if (user) {
            updateUser(user.id, { isOnline: true, socketId: socket.id });
            const { password, socketId, ...userData } = user;
            socket.emit('session_restored', { success: true, user: userData });
            broadcastUsers();
        }
    });

    // –í—Ö–æ–¥
    socket.on('login', (data) => {
        const users = getUsers();
        const user = users.find(u => u.username === data.username && u.password === data.password);
        
        if (!user) {
            socket.emit('login_error', '–ù–µ–≤–µ—Ä–Ω—ã–π –ª–æ–≥–∏–Ω –∏–ª–∏ –ø–∞—Ä–æ–ª—å');
            return;
        }

        updateUser(user.id, { isOnline: true, socketId: socket.id });
        const { password, socketId, ...userData } = user;
        socket.emit('login_success', { user: userData });
        broadcastUsers();
    });

    // –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è
    socket.on('register', (data) => {
        const users = getUsers();
        
        if (users.find(u => u.username === data.username)) {
            socket.emit('registration_error', '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç');
            return;
        }

        const newUser = {
            id: generateId(),
            username: data.username,
            password: data.password,
            role: data.role || 'user',
            displayName: data.username,
            avatar: 'üë§',
            rating: 0,
            ratingCount: 0,
            isOnline: true,
            socketId: socket.id,
            createdAt: new Date().toISOString()
        };

        users.push(newUser);
        saveUsers(users);
        
        const { password, socketId, ...userData } = newUser;
        socket.emit('registration_success', { user: userData });
        broadcastUsers();
    });

    // –î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Å–æ—Ç—Ä—É–¥–Ω–∏–∫–∞
    socket.on('register_staff', (data) => {
        const users = getUsers();
        
        if (users.find(u => u.username === data.username)) {
            socket.emit('staff_register_error', '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç');
            return;
        }

        const newStaff = {
            id: generateId(),
            username: data.username,
            password: data.password,
            role: data.role,
            displayName: data.displayName,
            avatar: data.role === 'admin' ? '‚öôÔ∏è' : 'üéß',
            rating: 0,
            ratingCount: 0,
            isOnline: false,
            socketId: null,
            createdAt: new Date().toISOString()
        };

        users.push(newStaff);
        saveUsers(users);
        
        const { password, socketId, ...staffData } = newStaff;
        socket.emit('staff_registered', { user: staffData });
        broadcastUsers();
    });

    // –°–º–µ–Ω–∞ —Ä–æ–ª–∏
    socket.on('change_role', (data) => {
        const updatedUser = updateUser(data.userId, { 
            role: data.newRole,
            avatar: data.newRole === 'admin' ? '‚öôÔ∏è' : data.newRole === 'listener' ? 'üéß' : 'üë§'
        });
        
        if (updatedUser) {
            const { password, socketId, ...userData } = updatedUser;
            socket.emit('role_changed', { userId: data.userId, newRole: data.newRole, user: userData });
            broadcastUsers();
        }
    });

    // –°–æ–∑–¥–∞–Ω–∏–µ —á–∞—Ç–∞
    socket.on('create_chat', (data) => {
        const chats = getChats();
        const user1 = getUserById(data.user1);
        const user2 = getUserById(data.user2);
        
        if (!user1 || !user2) {
            socket.emit('chat_create_error', '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω');
            return;
        }

        const existingChat = chats.find(chat => 
            chat.isActive && 
            ((chat.user1 === data.user1 && chat.user2 === data.user2) || 
             (chat.user1 === data.user2 && chat.user2 === data.user1))
        );

        if (existingChat) {
            socket.emit('chat_exists', { chat: existingChat });
            return;
        }

        const newChat = {
            id: generateId(),
            user1: data.user1,
            user2: data.user2,
            messages: [],
            startTime: new Date().toISOString(),
            isActive: true,
            createdAt: new Date().toISOString()
        };

        chats.push(newChat);
        saveChats(chats);

        socket.emit('chat_created', { 
            chat: newChat, 
            listenerName: user2.displayName 
        });

        // –£–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ —Å–ª—É—à–∞—Ç–µ–ª—è
        if (user2.socketId) {
            io.to(user2.socketId).emit('chat_created', { 
                chat: newChat, 
                userName: user1.displayName 
            });
        }

        broadcastChats();
    });

    // –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è
    socket.on('send_message', (data) => {
        const chats = getChats();
        const chat = chats.find(c => c.id === data.chatId);
        
        if (!chat) return;

        const newMessage = {
            id: generateId(),
            text: data.message.text,
            senderId: data.message.senderId,
            timestamp: new Date().toISOString(),
            type: 'text'
        };

        if (!chat.messages) chat.messages = [];
        chat.messages.push(newMessage);
        saveChats(chats);

        // –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è —É—á–∞—Å—Ç–Ω–∏–∫–∞–º —á–∞—Ç–∞
        const targetUserId = data.message.senderId === chat.user1 ? chat.user2 : chat.user1;
        const targetUser = getUserById(targetUserId);
        
        if (targetUser && targetUser.socketId) {
            io.to(targetUser.socketId).emit('new_message', { 
                chatId: data.chatId, 
                message: newMessage 
            });
        }

        // –û—Ç–ø—Ä–∞–≤–∫–∞ –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—é –¥–ª—è –º–≥–Ω–æ–≤–µ–Ω–Ω–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
        socket.emit('new_message', { 
            chatId: data.chatId, 
            message: newMessage 
        });

        broadcastChats();
    });

    // –ú–æ–¥–µ—Ä–∞—Ü–∏—è
    socket.on('apply_moderation', (data) => {
        const user = getUserById(data.userId);
        if (!user) return;

        let updates = {};
        switch (data.action) {
            case 'block':
                updates.isBlocked = true;
                break;
            case 'remove_block':
                updates.isBlocked = false;
                break;
            case 'warning':
                updates.warnings = (user.warnings || 0) + 1;
                break;
            case 'remove_warning':
                updates.warnings = Math.max(0, (user.warnings || 0) - 1);
                break;
            case 'vacation':
                updates.isOnVacation = true;
                break;
        }

        updateUser(data.userId, updates);
        socket.emit('moderation_applied', { record: data });
        broadcastUsers();
    });

    // –ó–∞–ø—Ä–æ—Å—ã –¥–∞–Ω–Ω—ã—Ö
    socket.on('get_users', () => {
        socket.emit('users_list', { users: getUsers().map(u => {
            const { password, socketId, ...user } = u;
            return user;
        })});
    });

    socket.on('get_chats', () => {
        socket.emit('chats_list', { chats: getChats() });
    });

    // –û—Ç–∫–ª—é—á–µ–Ω–∏–µ
    socket.on('disconnect', () => {
        const users = getUsers();
        const user = users.find(u => u.socketId === socket.id);
        if (user) {
            updateUser(user.id, { isOnline: false, socketId: null });
            broadcastUsers();
        }
    });
});

// –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏
function broadcastUsers() {
    io.emit('users_list', { users: getUsers().map(u => {
        const { password, socketId, ...user } = u;
        return user;
    })});
}

function broadcastChats() {
    io.emit('chats_list', { chats: getChats() });
}

